    /**
     * ActionPerformed del Button Accetta Reso
     * @param evt 
     */
    private void btnAccettaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccettaActionPerformed
        if(((BibliotecaGUI)f).getBiblioteca().rendiLibro(p)){
            p.setFinePrestito(LocalDateTime.now());
            JOptionPane.showMessageDialog(null, "Reso effettuato!", "RESO", JOptionPane.INFORMATION_MESSAGE);
            ((BibliotecaGUI)f).aggiornaTabella(2); //Aggiorno la tabella dei prestiti
            ((BibliotecaGUI)f).aggiornaTabella(1); //Aggiorno la tabella dei libri
        }else{
            JOptionPane.showMessageDialog(null, "Non Ã¨ stato possibile effettuare il reso!", "RESO", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAccettaActionPerformed


    /**
     * Metodo che setta le Label con i dati del prestito
     * @param p 
     */
    private void setLabels(Prestito p){
        //Ottengo il riferimento dell'utente
        Utente u = p.getUtente();
        //Ottengo il riferimento del libro
        Libro l = p.getLibro();
        
        //Setto le label dei dati dell'utente
        labelNome.setText(u.getNome() + " " + u.getCognome());
        labelData.setText( u.getData().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));
        labelTelefono.setText(u.getTelefono());
        
        //Setto le label dei dati del libro
        labelTitolo.setText(l.getTitolo());
        labelAutore.setText(l.getAutore());
        labelAnno.setText(String.valueOf(l.getAnno()));
        labelPrestito.setText(p.getInizioPrestito().format(DateTimeFormatter.ofPattern("HH:mm dd-MMM-yyyy")));
        labelIbsn.setText(l.getIbsn());
    }


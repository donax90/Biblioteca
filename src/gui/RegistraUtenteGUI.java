    /**
     * Metodo che data una stringa in input, la converte in una data di tipo LocalDate
     * @param data
     * @return data convertita
     */
    private LocalDate dataFormattata(String data){
        DateTimeFormatter format = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        return LocalDate.parse(data, format);
    }

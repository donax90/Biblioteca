    /**
     * ActionPerformed del Button Registra
     * @param evt 
     */
    private void btnRegistraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistraActionPerformed
        //Controllo se tutti i campi sono stati inseriti
        if(!nomeText.getText().isEmpty() && !cognomeText.getText().isEmpty() && !dataText.getText().isEmpty() && !telefonoText.getText().isEmpty()){
            //Sono stati tutti inseriti
            //Creo un nuovo Utente da aggiungere alla lista
            Utente utente = new Utente();
            utente.setNome(nomeText.getText());
            utente.setCognome(cognomeText.getText());
            utente.setData(dataFormattata(dataText.getText()));
            utente.setTelefono(telefonoText.getText());
            
            ((BibliotecaGUI)f).getBiblioteca().getListaUtenti().add(utente);//Aggiungo l'utente alla lista
            ((BibliotecaGUI)f).aggiornaTabella(0);//Aggiorno la tabella degli utenti
            
            JOptionPane.showMessageDialog(null, "Utente registrato correttamente!", "REGISTRAZIONE", JOptionPane.INFORMATION_MESSAGE);
            
            
        }else{
            //Manca qualche dato
            JOptionPane.showMessageDialog(null, "Perfavore inserisci tutti i campi!", "ATTENZIONE", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistraActionPerformed

